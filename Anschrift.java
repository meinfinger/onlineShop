package htl.swp1.klasse2.OPP.onlineShop;

public class Anschrift {
    private String PLZ;
    private String Ort;
    private String straße;
    private String hausnummer;

    public Anschrift(){}

    public Anschrift(String PLZ, String ort, String straße, String hausnummer) {
        this.PLZ = PLZ;
        this.ort = ort;
        this.straße = straße;
        this.hausnummer = hausnummer;
    }

}
